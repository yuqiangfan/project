    public void populateMaterialTable(Diet hairStyle) {
        DefaultTableModel model = (DefaultTableModel)materialJTable.getModel();
        model.setRowCount(0);
        
        for (Material material : hairStyle.getMaterialDirectory().getMaterialList()) {
            if(material.getQuantity()!=0) {
                Object[] row = new Object[2];
                row[0] = material.getName();
                // accurate to 2 decimal places
                row[1] = String.format("%d", material.getQuantity());//%.2f
            
                model.addRow(row);
            }
        }
    }
    
    public void populateProcessRequestTable() {
        DefaultTableModel model = (DefaultTableModel)processRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[5];
            row[0] = request;
            int quantity = ((DishRequest) request).getQuantity();
            row[1] = quantity;          
            String senderEnterprise = ((DishRequest) request).getSenderEnterprise() == null ? null : ((DishRequest) request).getSenderEnterprise().getName();
            row[2] = senderEnterprise;
            //row[3] =((DishRequest) request).getReceiverEnterprise().getName();
            String receiverEnterprise = ((DishRequest) request).getReceiverEnterprise() == null ? null : ((DishRequest) request).getReceiverEnterprise().getName();
            row[3] = receiverEnterprise;
            row[4] = request.getStatus();
            
            model.addRow(row);
        }
    }
    
