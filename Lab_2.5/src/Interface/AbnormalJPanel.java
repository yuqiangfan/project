    private void populateTable() {
        ArrayList<VitalSigns> abnormalList = new ArrayList<VitalSigns>();
        abnormalList = vsh.getAbnormalList(maxBP,minBP);
        DefaultTableModel dtm = (DefaultTableModel) vitalSignsTable.getModel();
        dtm.setRowCount(0);
        for (VitalSigns vs : abnormalList) {
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            dtm.addRow(row);
        }
        
    }
    
