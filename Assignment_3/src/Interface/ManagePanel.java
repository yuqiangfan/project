    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)tblDirectory.getModel();
        dtm.setRowCount(0);
        for(Car a : carList.getCarInformationList()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=a;
            row[1] = a.getBrand();
            row[2] = a.getMin_seats();
            row[3] = a.getMax_seats();
            dtm.addRow(row);
            
        }
    }
    
    public void populateResult(ArrayList<Car> list){
        if (! list.isEmpty()){
            DefaultTableModel dtm = (DefaultTableModel)tblShow.getModel();
            dtm.setRowCount(0);
            for(Car result : list){
                Object[] row = new Object[dtm.getColumnCount()];
                row[0] = result;
                row[1] = result.getBrand();
                row[2] = result.getManufactured_year();
                row[3] = result.getMin_seats();
                row[4] = result.getMax_seats();
                row[5] = result.getModel_num();
                row[6] = result.getAvailble_city();
                dtm.addRow(row);
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "No match results!", "Search Results", JOptionPane.WARNING_MESSAGE);
        }
    }

