    private void saveNewAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveNewAirlinerBtnActionPerformed
        // TODO add your handling code here:
        //Validation
        String airlinerName = airlinerNameTxt.getText();
        String iataCode = iataCodeTxt.getText();
        
        if (airlinerName == null || airlinerName.equals("")){
            airlinerNameTxt.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showConfirmDialog(null, "Airliner Name can't be empty!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
         else {
             airlinerNameTxt.setBorder(BorderFactory.createLineBorder(Color.green));
        }
          if (iataCode == null || iataCode.equals("")){
            iataCodeTxt.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showConfirmDialog(null, "Iata Code can't be empty!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
           else {
             iataCodeTxt.setBorder(BorderFactory.createLineBorder(Color.green));
        }
        if (airlinerName == null || airlinerName.equals("")){
            airlinerNameTxt.setBorder(BorderFactory.createLineBorder(Color.RED));
            jLabel1.setForeground(Color.red);
            JOptionPane.showMessageDialog(null,"Airliner Name can not be empty!");
            return;
        }
        if (iataCode == null || iataCode.equals("")){
            iataCodeTxt.setBorder(BorderFactory.createLineBorder(Color.RED));
            jLabel1.setForeground(Color.red);
            JOptionPane.showMessageDialog(null,"Iata Code can not be empty!");
            return;
        }
        //public AirlinerDirectory airlinerDir;
        //Airliner airliner = airlinerDir.addAirliner();
//        Airliner airliner = travelAgency.getAirlinerDir().addAirliner();

        Airliner airliner = travelAgency.getAirlinerDir().addAirliner();
        
        airliner.setAirlinerName(airlinerNameTxt.getText());
        airliner.setIataCode(iataCodeTxt.getText());
        // Create new flightSchedule for the airliner
        FlightSchedule fSCatalog = new FlightSchedule();
        airliner.setFSCatalog(fSCatalog);
        fSCatalog = travelAgency.getMTS().addFlightSchedule();
        // Pass the airliner object to next cardlayout
        this.airliner = airliner;
        JOptionPane.showMessageDialog(null, "Airliner Successfully created.");
    }//GEN-LAST:event_saveNewAirlinerBtnActionPerformed

