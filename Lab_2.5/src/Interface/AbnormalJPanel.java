    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
       int selectedRow = vitalSignsTable.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSigns vs = (VitalSigns)vitalSignsTable.getValueAt(selectedRow, 0);
            double temperature = Double.parseDouble(tempTextField.getText());
            double bp = Double.parseDouble(bloodTextField.getText());
            int pulse = Integer.parseInt(pulseTextField.getText());
            String date = dateTextField.getText();
            vs.setBloodPressure(bp);
            vs.setTemperature(temperature);
            vs.setDate(date);
            vs.setPulse(pulse);
            JOptionPane.showMessageDialog(null,"Vital Sign Updated");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
        
        tempTextField.setEnabled(false);
        bloodTextField.setEnabled(false);
        pulseTextField.setEnabled(false);
        dateTextField.setEnabled(false);
                                            
    }//GEN-LAST:event_btnConfirmActionPerformed

        

