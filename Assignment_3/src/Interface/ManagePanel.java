    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDirectory.getSelectedRow();
        if(selectedRow >= 0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Car car = (Car)tblDirectory.getValueAt(selectedRow, 0);
                carList.deleteCar(car);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row from main table!!");
        }
        carList.setDate(new Date());
    }//GEN-LAST:event_btnDeleteActionPerformed

